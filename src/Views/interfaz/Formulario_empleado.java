    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed

        int sue = 0, boni = 0, neto = 0, rete = 0;
        switch (this.jComboBox4.getSelectedIndex()) {
            case 0:
                switch (this.jComboBox5.getSelectedIndex()) {
                    case 0:
                        sue = 1200;
                        break;
                    case 1:
                        sue = 1400;
                        break;
                    case 2:
                        sue = 1600;
                        break;
                }

            case 1:
                switch (this.jComboBox5.getSelectedIndex()) {
                    case 0:
                        sue = 700;
                        break;
                    case 1:
                        sue = 800;
                        break;
                    case 2:
                        sue = 1000;
                        break;
                }
        }

        //determinar la bonificacion
        switch (this.jComboBox6.getSelectedIndex()) {
            case 0:
                boni = sue * 25 / 100;
                break;
            case 1:
                boni = sue * 20 / 100;
                break;
            case 2:
                boni = sue * 18 / 100;
                break;
            case 3:
                boni = sue * 15 / 100;
                break;
            case 4:
                boni = sue * 12 / 100;
                break;
            case 5:
                boni = sue * 10 / 100;
                break;

        }

        //deteminar las retenciones
        switch (this.jComboBox5.getSelectedIndex()) {
            case 0:
                rete = sue * 12 / 100;
                break;
            case 1:
                rete = sue * 8 / 100;
                break;
            case 2:
                rete = sue * 6 / 100;
                break;

        }

        //determinar el neto apagar
        neto = boni + sue - rete;

        //mostrando datos
        this.jTextField27.setText("" + sue);
        this.jTextField28.setText("" + boni);
        this.jTextField29.setText("" + rete);
        this.jTextField30.setText("" + neto);

    }//GEN-LAST:event_jButton22ActionPerformed

